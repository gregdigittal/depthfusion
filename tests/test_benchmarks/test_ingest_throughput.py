"""T-603 — Ingestion throughput benchmark: >=20 docs/s on 50 synthetic small PDFs.

The benchmark generates 50 small but valid PDF files using pypdf (an existing
depthfusion dependency), then runs IngestPipeline over them sequentially and
asserts that the measured throughput meets or exceeds 20 documents per second.

The 20 docs/s floor is deliberately conservative — production hardware and even
a CI container comfortably exceed this on text-extraction from small PDFs.  The
threshold exists to catch regressions in the ingest hot-path (e.g. accidental
O(n²) loops or synchronous network calls) rather than to enforce a fixed
latency SLA.

Acceptance criteria (T-603):
  - Benchmark runs over exactly 50 synthetic PDFs.
  - Measured throughput (docs / wall-clock seconds) >= 20 docs/s.
  - Each PDF is a distinct, properly-formed PDF file parseable by pypdf.

Design notes:
  - PDFs are generated on-the-fly with pypdf's PdfWriter (zero external deps).
  - The pipeline is constructed with no embed/store callbacks so only the
    parse + chunk path is exercised (the hot path).
  - Wall-clock time is measured with time.perf_counter() around the ingest loop.
  - The test is collected in the default CI run (not marked 'integration').
"""
from __future__ import annotations

import io
import pathlib
import time

import pytest
from pypdf import PdfWriter

from depthfusion.ingest.pipeline import IngestPipeline

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

NUM_DOCS: int = 50
THROUGHPUT_FLOOR_DOCS_PER_S: float = 20.0

# ---------------------------------------------------------------------------
# Fixture: synthetic PDF corpus
# ---------------------------------------------------------------------------


@pytest.fixture(scope="module")
def synthetic_pdf_corpus(tmp_path_factory: pytest.TempPathFactory) -> list[pathlib.Path]:
    """Create NUM_DOCS small valid PDF files and return their paths.

    Each PDF is generated by pypdf's PdfWriter with:
      - One blank A4 page (no rasterised images — keeps file size small).
      - A unique /Title metadata entry so each file has a distinct content_hash.

    The files are created once per module and shared across all tests in this
    module (scope='module') to avoid repeated I/O during the benchmark itself.
    """
    corpus_dir = tmp_path_factory.mktemp("pdf_corpus")
    paths: list[pathlib.Path] = []

    for i in range(NUM_DOCS):
        pdf_path = corpus_dir / f"synthetic_{i:03d}.pdf"
        writer = PdfWriter()
        writer.add_blank_page(width=595, height=842)  # A4 in points
        # Unique metadata ensures distinct content hashes across documents.
        writer.add_metadata(
            {
                "/Title": f"Synthetic benchmark document {i:03d}",
                "/Subject": f"T-603 ingest throughput test — corpus item {i}",
                "/Keywords": f"benchmark synthetic doc{i}",
            }
        )
        buf = io.BytesIO()
        writer.write(buf)
        pdf_path.write_bytes(buf.getvalue())
        paths.append(pdf_path)

    assert len(paths) == NUM_DOCS, (
        f"Expected {NUM_DOCS} PDFs in corpus, got {len(paths)}"
    )
    return paths


# ---------------------------------------------------------------------------
# Benchmark test
# ---------------------------------------------------------------------------


class TestIngestThroughput:
    """Throughput benchmark for the IngestPipeline (T-603)."""

    def test_throughput_meets_floor(
        self, synthetic_pdf_corpus: list[pathlib.Path]
    ) -> None:
        """Ingesting 50 small PDFs must achieve >= 20 docs/s.

        The pipeline runs with no embed or store callbacks — only parse + chunk
        are on the critical path.  This isolates the depthfusion hot-path from
        any vector store or embedding model latency.
        """
        pipeline = IngestPipeline()

        # Warm-up: parse the first document outside the timed window so
        # any one-time import costs (pypdf module caching, etc.) do not
        # skew the measurement.
        pipeline.run(str(synthetic_pdf_corpus[0]), "application/pdf")

        # Timed window: remaining 49 documents (still enough to be statistically
        # meaningful at this floor).
        bench_paths = synthetic_pdf_corpus  # time the full 50 for the assertion

        start_ts = time.perf_counter()
        for pdf_path in bench_paths:
            doc = pipeline.run(str(pdf_path), "application/pdf")
            # Sanity: parser must return a ParsedDocument (not None).
            assert doc is not None, (
                f"IngestPipeline.run() returned None for {pdf_path}; "
                "expected a ParsedDocument when no file_index is provided."
            )
        elapsed_s = time.perf_counter() - start_ts

        measured_throughput = len(bench_paths) / elapsed_s

        assert measured_throughput >= THROUGHPUT_FLOOR_DOCS_PER_S, (
            f"Ingestion throughput {measured_throughput:.1f} docs/s fell below "
            f"the {THROUGHPUT_FLOOR_DOCS_PER_S:.0f} docs/s floor "
            f"({len(bench_paths)} docs in {elapsed_s:.3f}s). "
            "Check for regressions in the parse/chunk hot-path."
        )

    def test_corpus_has_correct_size(
        self, synthetic_pdf_corpus: list[pathlib.Path]
    ) -> None:
        """Corpus must contain exactly NUM_DOCS files, all readable PDFs."""
        assert len(synthetic_pdf_corpus) == NUM_DOCS

        for pdf_path in synthetic_pdf_corpus:
            assert pdf_path.exists(), f"Corpus file missing: {pdf_path}"
            assert pdf_path.stat().st_size > 0, f"Corpus file is empty: {pdf_path}"

    def test_all_docs_parsed_without_error(
        self, synthetic_pdf_corpus: list[pathlib.Path]
    ) -> None:
        """Every synthetic PDF must parse to a non-None ParsedDocument."""
        pipeline = IngestPipeline()

        for pdf_path in synthetic_pdf_corpus:
            doc = pipeline.run(str(pdf_path), "application/pdf")
            assert doc is not None, (
                f"Parser returned None for {pdf_path} "
                "(expected ParsedDocument; no file_index supplied)"
            )
            assert isinstance(doc.source_id, str)
            assert isinstance(doc.text, str)  # text may be empty for blank pages

    def test_no_double_ingest_with_file_index(
        self, synthetic_pdf_corpus: list[pathlib.Path], tmp_path: pathlib.Path
    ) -> None:
        """With a FileMetadataIndex, repeated runs skip unchanged documents.

        This is a correctness guard: the throughput benchmark without a
        file_index re-ingests every document, but with a file_index the
        second pass should return None for all unchanged files (zero embed/store
        overhead).  Verifies T-602 is effective in the throughput path.
        """
        from unittest.mock import MagicMock

        from depthfusion.storage.file_index import FileMetadataIndex

        db_path = tmp_path / "bench_index.db"
        index = FileMetadataIndex(db_path=db_path)
        embed_mock = MagicMock()
        store_mock = MagicMock()

        pipeline = IngestPipeline(
            embed_callback=embed_mock,
            store_callback=store_mock,
            file_index=index,
        )

        # First pass — all docs ingested.
        first_pass_results = [
            pipeline.run(str(p), "application/pdf") for p in synthetic_pdf_corpus
        ]
        assert all(r is not None for r in first_pass_results), (
            "First-pass ingest should process all documents"
        )
        # Second pass — all identical; should be no-ops.
        embed_mock.reset_mock()
        store_mock.reset_mock()

        second_pass_results = [
            pipeline.run(str(p), "application/pdf") for p in synthetic_pdf_corpus
        ]
        assert all(r is None for r in second_pass_results), (
            "Second-pass ingest with identical content should return None for every doc"
        )
        assert embed_mock.call_count == 0, (
            "embed_callback must not be called on unchanged documents"
        )
        assert store_mock.call_count == 0, (
            "store_callback must not be called on unchanged documents"
        )

        index.close()
